print (10 + 3)  # + is the addition operator
print (10 - 3)  # - is the subtraction operator
print (10 * 3)  # * is the multiplication operator
print (10 / 3)  # / is the division operator (output is decimal)
print (10 // 3) # // is also a division operator but the OUTPUT IS AN INTEGER
print (10 % 3)  # % is the modulus operator which gives the remainder of the division of the numbers
print (10 ** 3) # ** is the exponent operator

