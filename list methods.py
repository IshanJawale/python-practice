numbers = [1, 2, 3, 4, 5]
print(2 in numbers)  # finds out if the said element exists in the list
print(len(numbers))  # gives the number of elements in the list
numbers.append(6)  # adds an element at the end of the list
print(numbers)
numbers.insert(3, -1)  # adds element after the given index
print(numbers)
numbers.remove(3)  # removes the element three
print(numbers)
numbers.pop()     # removes the last element in the list
print(numbers)
numbers.clear()  # empties the list i.e clears all the items
print(numbers)
