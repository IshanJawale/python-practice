names = ["John", "Bob", "Mosh", "Ishan", "Mary"]
names [0] = "Jon"  # changes one of the elements of the list
print(names)        # prints the whole list
print(names[0])     # prints the first element
print(names[1])     # prints the second element
print(names[-1])    # prints the last element
print(names[-2])    # prints the second last element
print(names[0:3])  # prints all the elements from 0 to 2 index, it excludes the end index
print(names[2:])   # prints all the element from the element having the index 2
print(names[1:4])  # prints the elements from the element having the index 1 to the element having index 3 but not the one having index 4



